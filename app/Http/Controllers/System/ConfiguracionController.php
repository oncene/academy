    public function update(Request $request)
    {
        if ($request->ajax()) {
            $conf_id = \Illuminate\Support\Facades\Auth::user()->configuracion[0]['id'];
            $data = Configuracion::findOrFail($conf_id);
            $data->user_id=Auth::user()->id;
            $data->color_id = $request->get('color_id');
            $data->save();
        }
    }

